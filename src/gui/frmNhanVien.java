    private void loadNhanVienData() {
        DefaultTableModel model = (DefaultTableModel) tb.getModel();
        model.setRowCount(0); // Clear table
        List<NhanVien> list = nhanVienDAO.getAllNhanVien();
        for (NhanVien nv : list) {
            model.addRow(new Object[]{nv.getMaNV(), nv.getTenNV(), nv.getNgaySinh(), nv.getSdt(), nv.getLoaiNV(), true});
        }
    }
    
    private void addTableMouseListener() {
        tb.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent evt) {
                int selectedRow = tb.getSelectedRow();
                if (selectedRow != -1) {
                    populateFormComponents(selectedRow);
                }
            }
        });
    }

    private void populateFormComponents(int row) {
        String maNV = tb.getValueAt(row, 0).toString();
        String tenNV = tb.getValueAt(row, 1).toString();
        String ngaySinh = tb.getValueAt(row, 2).toString();
        String sdt = tb.getValueAt(row, 3).toString();
        String chucVu = tb.getValueAt(row, 4).toString();

        txtMaNV.setText(maNV);
        txtTenNV.setText(tenNV);
        txtSDT.setText(sdt);
        cboChucVu.setSelectedItem(chucVu);
        
        try {
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse(ngaySinh);
            ngaySinh_DateChooser.setSelectedDate(new java.util.GregorianCalendar(date.getYear() + 1900, date.getMonth(), date.getDate()));
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

