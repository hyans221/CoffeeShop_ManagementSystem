    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String searchValue = txtSearch.getText();
        DefaultTableModel model = (DefaultTableModel) tb.getModel();
        model.setRowCount(0); // Clear table
        List<NhanVien> list = nhanVienDAO.searchNhanVienByName(searchValue);
        for (NhanVien nv : list) {
            model.addRow(new Object[]{nv.getMaNV(), nv.getTenNV(), nv.getSdt(), nv.getLoaiNV(), true});
        }

    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try {
            String maNV = txtMaNV.getText();
            String tenNV = txtTenNV.getText();
            String sdt = txtSDT.getText();
            Date ngaySinhDate = ngaySinh_DateChooser.getSelectedDate().getTime();
            String ngaySinh = new SimpleDateFormat("yyyy-MM-dd").format(ngaySinhDate);
            String chucVu = cboChucVu.getSelectedItem().toString();

            NhanVien nv = new NhanVien(maNV, tenNV, sdt, ngaySinh, chucVu);
            boolean success = nhanVienDAO.insertNhanVien(nv);
            if (success) {
                JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công!");
                loadNhanVienData();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm nhân viên thất bại!");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Có lỗi xảy ra khi thêm nhân viên!");
        }

    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int selectedRow = tb.getSelectedRow();
        if (selectedRow != -1) {
            String maNV = tb.getValueAt(selectedRow, 0).toString();
            boolean success = nhanVienDAO.deleteNhanVien(maNV);
            if (success) {
                JOptionPane.showMessageDialog(this, "Xóa nhân viên thành công!");
                loadNhanVienData();
            } else {
                JOptionPane.showMessageDialog(this, "Xóa nhân viên thất bại!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên cần xóa!");
        }

    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int selectedRow = tb.getSelectedRow();
        if (selectedRow != -1) {
            try {
                String maNV = txtMaNV.getText();
                String tenNV = txtTenNV.getText();
                String sdt = txtSDT.getText();
                Date ngaySinhDate = ngaySinh_DateChooser.getSelectedDate().getTime();
                String ngaySinh = new SimpleDateFormat("yyyy-MM-dd").format(ngaySinhDate);
                String chucVu = cboChucVu.getSelectedItem().toString();

                NhanVien nv = new NhanVien(maNV, tenNV, sdt, ngaySinh, chucVu);
                boolean success = nhanVienDAO.updateNhanVien(nv);
                if (success) {
                    JOptionPane.showMessageDialog(this, "Cập nhật thông tin nhân viên thành công!");
                    loadNhanVienData();
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật thông tin nhân viên thất bại!");
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Có lỗi xảy ra khi cập nhật thông tin nhân viên!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên cần cập nhật!");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

